    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       String Tipo ;
       if(rbNIF.isSelected()== true){
           Tipo = "NIF";
       }else if(rbNIE.isSelected()== true){
           Tipo = "NIE";
       }else if(rbCIF.isSelected()== true){
           Tipo = "CIF";
       }else {
           Tipo = "NIF";
       }
       String NIF_NIE_CIF = txtNIF_NIE_CIF.getText();
       String MSISDN = txtMSISDN.getText();
       String TarifaActual = txtTarifaActual.getText();
       String TarifaDestino = txtTarifaDestino.getText();
       String JiraCC = txtJiraCC.getText();
       String Comentarios = txtComentarios.getText();
       
       try{
           Connection con = Conexion.getConexion();
           PreparedStatement ps = con.prepareStatement("INSERT INTO AlarmadoSTC(Tipo,NIF_NIE_CIF,MSISDN,Tarifa_Actual,Tarifa_Destino,JiraCC,Comentarios, activo) VALUES (?,?,?,?,?,?,?,?)");
           ps.setString(1, Tipo);
           ps.setString(2, NIF_NIE_CIF);
           ps.setString(3, MSISDN);
           ps.setString(4, TarifaActual);
           ps.setString(5, TarifaDestino);
           ps.setString(6, JiraCC);
           ps.setString(7, Comentarios);
           ps.setInt(8, 1);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null, "Registro guardado");
           limpiar();
           cargarTabla();
           
       } catch(SQLException e){
           JOptionPane.showMessageDialog(null, e.toString());
       }
       
       
       
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblAlarmadoSTCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlarmadoSTCMouseClicked
        
        try{
            int fila = tblAlarmadoSTC.getSelectedRow();
            int Id_STC = Integer.parseInt(tblAlarmadoSTC.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("select Tipo,NIF_NIE_CIF,MSISDN,Tarifa_Actual,Tarifa_Destino,JiraCC,Comentarios from AlarmadoSTC where Id_STC=?");
            ps.setInt(1, Id_STC);
            rs = ps.executeQuery();
            
            while(rs.next()){
                txtId_STC.setText(String.valueOf(Id_STC));
                txtNIF_NIE_CIF.setText(rs.getString("NIF_NIE_CIF"));
                txtMSISDN.setText(rs.getString("MSISDN"));
                txtTarifaActual.setText(rs.getString("Tarifa_Actual"));
                txtTarifaDestino.setText(rs.getString("Tarifa_Destino"));
                txtJiraCC.setText(rs.getString("JiraCC"));
                txtComentarios.setText(rs.getString("Comentarios"));
                if(rs.getString("Tipo").equals("NIF")){
                    rbNIF.setSelected(true);
                } else if(rs.getString("Tipo").equals("NIE")){
                    rbNIE.setSelected(true);
                } else if(rs.getString("Tipo").equals("CIF")){
                    rbCIF.setSelected(true);
                }
            }
            
        } catch(SQLException e){
           JOptionPane.showMessageDialog(null, e.toString());
        
        }
    }//GEN-LAST:event_tblAlarmadoSTCMouseClicked

