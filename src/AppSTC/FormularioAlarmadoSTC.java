    private void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblAlarmadoSTC.getModel();
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        /*int[] anchos = {10, 50, 12, 12, 100};
        for(int i=0; i< tblAlarmadoSTC.getColumnCount(); i++){
            tblAlarmadoSTC.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }*/
        
        try{
            
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("select Id_STC,Tipo,NIF_NIE_CIF,MSISDN,Tarifa_Actual,Tarifa_Destino,JiraCC,Comentarios from AlarmadoSTC");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[columnas];
                for(int indice=0; indice<columnas; indice++){
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }
            
        } catch(SQLException e){
           JOptionPane.showMessageDialog(null, e.toString());
        
        }
        
        
    }
    
    
    
